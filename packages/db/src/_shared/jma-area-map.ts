/**
 * 都道府県名から JMA 府県予報区コードを引く。表記ゆれ（前後空白）を吸収する。未知の府県は null。
 * 純関数（テスト容易）。実 DB 読み取りは呼び出し側（Job ドライバ / サイネージ読取）が行う。
 *
 * @param prefecture `schools.prefecture` の値（例: "岐阜県"）。
 * @returns JMA 府県予報区コード（例: "210000"）。未知なら null（Job 側で skip + 監視ログ）。
 */
export function resolveJmaAreaCode(prefecture: string | null | undefined): string | null {
  if (typeof prefecture !== "string") return null;
  const key = prefecture.trim();
  if (key.length === 0) return null;
  return PREFECTURE_TO_JMA_AREA_CODE[key] ?? null;
}
